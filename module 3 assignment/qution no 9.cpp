/* 9.find the circumference of triangle formula : triangle = a+b+c */



#include<stdio.h>
int main(){
	
	int a,b,c,triangle;
	
	printf("\n enter the value of a");
	scanf("%d",&a);
	
	printf("\n enter the value of b");
	scanf("%d",&b);
	
	printf("\n enter the value of c");
	scanf("%d",&c);
	
	// circumference of triangle 
	
	triangle=a+b+c;
	
	printf("\n the circumference of triangle %d",triangle);
	
}
